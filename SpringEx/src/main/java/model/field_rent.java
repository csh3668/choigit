package model;

public class field_rent {
	int rent_num;
	String rent_title;
	String rent_content;
	String rent_date;
	
	public int getRent_num() {
		return rent_num;
	}
	public void setRent_num(int rent_num) {
		this.rent_num = rent_num;
	}
	public String getRent_title() {
		return rent_title;
	}
	public void setRent_title(String rent_title) {
		this.rent_title = rent_title;
	}
	public String getRent_content() {
		return rent_content;
	}
	public void setRent_content(String rent_content) {
		this.rent_content = rent_content;
	}
	public String getRent_date() {
		return rent_date;
	}
	public void setRent_date(String rent_date) {
		this.rent_date = rent_date;
	}
}
